package movies;

public class Movie {

    private String name;
    private String category;

    public Movie(String name, String category) {

        this.name = name;
        this.category = category;

    }

    public void setName() {

        this.name = name;

    }

    public String getName() {

        return name;

    }

    public void setCategory() {

        this.category = category;

    }

    public String getCategory() {

        return category;

    }

}